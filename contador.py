'''

O que é uma estrutura de repetição?

Serve para executar um bloco de código várias vezes 
Enquanto uma condição é verdadeira


'''

'''     

Lógica tabuada

5*1=5
5*2=10
5*3=15

'''

tabuada = 5
contador = 0 

while contador <= 10:
    print(tabuada, "x", contador, "=", contador * tabuada)
    contador += 1